async function main() {
  console.log("🗑️  Removing all existing news...");
  const removed = await prisma.news.deleteMany({});
  console.log(`   Deleted ${removed.count} news items.`);

  console.log("📰 Creating fresh IT news entry...");
  await prisma.news.create({ data: NEWS });
  console.log(`   Created: ${NEWS.title}`);

  console.log("\n✅ Done.");
}

main()
  .catch((e) => { console.error("❌ Reset failed:", e); process.exit(1); })
  .finally(async () => { await prisma.$disconnect(); await pool.end(); });
