import "dotenv/config";
import { PrismaClient } from "@prisma/client";
import { PrismaPg } from "@prisma/adapter-pg";
import { Pool } from "pg";

const pool = new Pool({ connectionString: process.env.DIRECT_URL || process.env.DATABASE_URL! });
const prisma = new PrismaClient({ adapter: new PrismaPg(pool) });

async function main() {
  const existing = await prisma.news.findFirst({ where: { title: { contains: "HTML 101" } } });
  if (existing) {
    console.log(`ℹ️  News item already exists — skipping (id: ${existing.id})`);
    return;
  }

  await prisma.news.create({
    data: {
      title: "HTML 101 — Скелет страницы: курс с нуля",
      category: "Обновление",
      summary: "30 уроков по HTML — от первого тега до полностью свёрстанной страницы рецепта. Для тех, кто ни разу не делал сайтов. С автопроверкой и живым предпросмотром.",
      pinned: true,
      publishedAt: new Date(),
      imageUrl: null,
      body: `# HTML 101 — Скелет страницы

Запустили **полный курс HTML с нуля** для тех, кто никогда не делал сайтов.

## Что внутри

**30 уроков**, разбитых на блоки:

1. **Концепты** — что такое веб, как браузер читает HTML, что такое тег и атрибут (3 урока, квизы)
2. **Текст** — заголовки, параграфы, переносы, жирный/курсив, цитаты (6 уроков)
3. **Ссылки и навигация** — внешние, внутренние, якорные (3 урока)
4. **Изображения** — img, форматы, адаптивные picture/srcset (3 урока)
5. **Списки** — маркированные, нумерованные, вложенные (3 урока)
6. **Таблицы** — структура, заголовки, объединение ячеек (4 урока)
7. **Документ и шапка** — doctype, head, meta, title, lang (3 урока)
8. **Семантика** — code, details, time, abbr, mark (3 урока)
9. **Финал** — свёрстать страницу рецепта со всеми изученными тегами

## Что нового в инфраструктуре

- **Живой предпросмотр** — справа от редактора показывается результат вживую, обновляется по мере того как ты пишешь
- **DOM-автопроверка** — тесты осмысленные: «Заголовок h1 содержит «Привет»», «В списке ровно 3 элемента», «Ссылка ведёт на example.com»
- **Безопасный sandbox** — все iframe запускаются без выполнения JS, ничего страшного никогда не произойдёт

## Как устроен каждый урок

- **🎯 Зачем это нужно** — метафоры из жизни (тег = коробка, атрибут = наклейка)
- **📚 Как пишется** — синтаксис с подписанными частями
- **🧪 Примеры** — разобранные фрагменты
- **⚠️ Частые ошибки** — типичные грабли (забыли закрыть тег, написали заглавными)
- **🛠️ Задание** — практика с автопроверкой

Каждый урок имеет **3 прогрессивных подсказки** — от мягкого намёка до полного решения.

## Что дальше

HTML 101 — первый из четырёх курсов веб-трека: **HTML 101 → HTML 102 (формы и семантика) → CSS 101 (внешний вид) → CSS 102 (layout и анимации)**. После всего трека ты сможешь свёрстать законченный адаптивный сайт.

Заходи в «Курсы» и начни с урока 1 — «Что такое веб-страница».`,
    },
  });
  console.log("✅ News item created: HTML 101 announcement");
}

main()
  .catch((e) => {
    console.error("❌ Seed failed:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
    await pool.end();
  });
