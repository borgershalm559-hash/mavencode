import "dotenv/config";
import { PrismaClient } from "@prisma/client";
import { PrismaPg } from "@prisma/adapter-pg";
import { Pool } from "pg";

const pool = new Pool({ connectionString: process.env.DIRECT_URL || process.env.DATABASE_URL! });
const prisma = new PrismaClient({ adapter: new PrismaPg(pool) });

async function main() {
  const existing = await prisma.news.findFirst({ where: { title: { contains: "Python 101" } } });
  if (existing) {
    console.log(`ℹ️  News item already exists — skipping (id: ${existing.id})`);
    return;
  }

  await prisma.news.create({
    data: {
      title: "Python 101 — Первые шаги: полный курс с нуля",
      category: "Обновление",
      summary: "20 уроков с подробной теорией, метафорами из жизни и прогрессивными подсказками. Для тех, кто ни разу не кодил. Итоговый проект — мини-записная книжка.",
      pinned: true,
      publishedAt: new Date(),
      imageUrl: null,
      body: `# Python 101 — Первые шаги

Готов **полный курс Python с нуля** — для тех, кто ни разу не программировал.

## Что внутри

**20 уроков, ~15-20 часов**, разбитых на 6 блоков:

1. **Введение** — что такое программирование, Hello World, порядок инструкций (3 урока)
2. **Данные и переменные** — переменные, числа, строки, bool (4 урока)
3. **Ветвление и циклы** — if/elif/else, while, for, найди-баг (4 урока)
4. **Структуры данных** — списки, словари, кортежи, множества, найди-баг (5 уроков)
5. **Функции** — def/return, параметры по умолчанию, область видимости (3 урока)
6. **Капстон** — мини-записная книжка, собирающая всё вместе (1 урок)

## Как устроен каждый урок

- **🎯 Что научишься делать** — конкретные навыки после урока
- **🔍 Зачем это нужно** — реальные применения в работе
- **📚 Теория** — 1500-2000 слов объяснений с метафорами из жизни
- **🧪 Примеры** — разобранный код построчно
- **⚠️ Частые ошибки новичков** — чтобы не наступать на грабли
- **💡 Мини-тест самопроверки** — вопросы и ответы
- **🛠️ Задание** — практика в редакторе с автопроверкой

Каждый код-урок имеет **стартовый каркас**, **4-6 автотестов** и **3 прогрессивных подсказки** (от мягкого намёка до полного решения с пояснениями).

## Что заменяется

Старый короткий курс «Python Основы» (6 уроков) заменён на Python 101. Если ты проходил старый — начни сначала, новый сильно подробнее.

## Что дальше

Python 101 — первый из планируемой **программы из 5 курсов** (101 → 201 → 301 → 401 → 501). После 101 ты уверенно пишешь простые скрипты и читаешь чужой код. Следующие курсы уже в разработке.

Заходи в раздел «Курсы» и начни с урока 1 — «Что такое программирование и зачем Python».`,
    },
  });
  console.log("✅ News item created: Python 101 announcement");
}

main()
  .catch((e) => {
    console.error("❌ Seed failed:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
    await pool.end();
  });
