async function main() {
  console.log("🌱 Seeding news...");
  // Clear existing news to avoid duplicates on re-run
  const deleted = await prisma.news.deleteMany({});
  console.log(`🗑️  Cleared ${deleted.count} existing news items`);

  for (const n of news) {
    const created = await prisma.news.create({ data: n });
    console.log(`✓ Created: ${created.title}`);
  }
  console.log(`\n✅ Done — ${news.length} news items added.`);
}

main()
  .catch((e) => {
    console.error("❌ Seed failed:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
    await pool.end();
  });
